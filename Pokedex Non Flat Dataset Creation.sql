-- CREATE TABLES

CREATE TABLE pokemon (
	id INT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    generation_id INT NOT NULL,
    total INT,
    legendary BOOLEAN,
    FOREIGN KEY (generation_id) REFERENCES generations(id)
    );
    
CREATE TABLE generations (
    id INT PRIMARY KEY,
    name VARCHAR(50),
    release_year INT
);

CREATE TABLE stats (
	id INT PRIMARY KEY,
    hp INT NOT NULL,
    attack INT NOT NULL,
    defense INT NOT NULL,
    sp_attack INT NOT NULL,
    sp_defense INT NOT NULL,
    speed INT NOT NULL,
    FOREIGN KEY(pokemon_id) REFERENCES pokemon(id)
);

CREATE TABLE types (
    id INT PRIMARY KEY,
    name VARCHAR(50) UNIQUE NOT NULL
);


CREATE TABLE pokemon_types (
    id INT PRIMARY KEY,
    type_id_1 INT NOT NULL,
    type_id_2 INT, -- Peut être NULL si le Pokémon a un seul type
    FOREIGN KEY (pokemon_id) REFERENCES pokemon(id),
    FOREIGN KEY (type_id_1) REFERENCES types(id),
    FOREIGN KEY (type_id_2) REFERENCES types(id)
);

CREATE TABLE pokemon_specs (
	id INT PRIMARY KEY,
    height DECIMAL(3,1) NOT NULL,
    weight DECIMAL(5,1) NOT NULL
);
	
